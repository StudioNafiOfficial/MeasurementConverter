# meters

def metersToMeters(meters):
    return meters

def metersToYards(meters):
    return meters * 1.094

def metersToFeet(meters):
    return meters * 1.094 * 3

def metersToInches(meters):
    return meters * 1.094 * 3 * 12

def metersToCentimeters(meters):
    return meters * 1.094 * 3 * 12 * 2.54

def metersToMilimeters(meters):
    return meters * 1.094 * 3 * 12 * 2.54 * 10


